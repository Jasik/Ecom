import Foundation

//Финальный босс! Мы добрались до самого верхнего, видимого пользователю уровня.
//
//Если **Domain** — это «мозг» (правила), а **Data** — это «руки» (добыча информации), то слой **Presentation (Презентация)** — это **«Лицо» и «Голос»** твоего приложения.
//
//Главная задача этого слоя — красиво показать данные пользователю и правильно отреагировать на его нажатия. Слой Presentation не принимает бизнес-решений и не знает, откуда берутся данные. Он работает исключительно с готовым результатом от Use Cases.
//
//В нашей архитектуре слой Presentation состоит из трех главных элементов: **Views (Экраны)**, **ViewModels (Модели представления)** и **Router (Навигация)**.
//
//---
//
//### 1. Views (Экраны / SwiftUI)
//**Что это:** Визуальные компоненты (кнопки, списки, текст).
//**Для чего:** Отображать состояние (State), которое предоставляет ViewModel, и передавать действия пользователя (нажатия, свайпы) во ViewModel или Router.
//
//**Правила написания:**
//* **«Тупой UI» (Dumb UI):** View должна быть максимально глупой. Она не умеет считать суммы, не умеет форматировать даты и не знает про сеть. Она просто рисует то, что ей дали.
//* **Изоляция SwiftUI:** Это единственное место во всем приложении, где мы пишем `import SwiftUI`.
//
//```swift
//// ИДЕАЛЬНАЯ VIEW
//@MainActor
//struct ProductRowView: View {
//    let product: Product // Принимаем готовую, чистую модель
//    
//    var body: some View {
//        VStack {
//            Text(verbatim: product.title).font(.headline)
//            // View не форматирует цену, она берет готовую строку!
//            Text(verbatim: product.formattedPrice).foregroundColor(.blue)
//        }
//    }
//}
//```
//
//---
//
//### 2. ViewModels (Состояние и Логика экрана)
//**Что это:** Классы (или структуры), которые служат "переводчиками" между View и Domain.
//**Для чего:** Они держат в себе состояние экрана (загрузка, список товаров, ошибки) и реагируют на действия из View, вызывая нужные Use Cases.
//
//В Swift 6 ViewModels кардинально изменились благодаря макросу `@Observable`.
//
//**Правила написания:**
//* **`@MainActor` обязателен:** Вся работа с UI-состоянием должна происходить на главном потоке. Swift 6 проверяет это сверхстрого.
//* **Никакого Combine:** Мы больше не используем `@Published` и `ObservableObject`. Макрос `@Observable` работает быстрее и не требует импорта `Combine`.
//* **DI через Конструктор:** Как мы уже выяснили, зависимости (Use Cases) передаются через `init`, чтобы экран можно было легко протестировать.
//
//```swift
//// ИДЕАЛЬНАЯ VIEWMODEL
//import Foundation // ❗️ Нет SwiftUI
//
//@MainActor @Observable
//public final class ProductDetailViewModel {
//    public var isAddedToCart = false // Состояние для View
//    private let addToCartUseCase: AddToCartUseCase // Ссылка на Домен
//    
//    // Designated Init (Для тестов)
//    public init(addToCartUseCase: AddToCartUseCase) {
//        self.addToCartUseCase = addToCartUseCase
//    }
//    
//    // Действие из View
//    public func onAddToCartTapped(product: Product) {
//        Task {
//            await addToCartUseCase.execute(product)
//            isAddedToCart = true // Обновляем UI
//        }
//    }
//}
//```
//
//---
//
//### 3. Router (Навигация)
//**Что это:** Класс, который управляет переходами между экранами.
//**Для чего:** Чтобы убрать жесткую связность экранов друг с другом.
//
//В классическом SwiftUI джуниоры часто пишут `NavigationLink(destination: CartView())`. Это огромная ошибка. Экран каталога не должен знать о существовании экрана корзины! Если завтра бизнес скажет: *"Перед открытием корзины покажи экран авторизации"*, тебе придется переписывать View.
//
//С Роутером `CatalogView` просто говорит: `router.presentSheet(.cart)`. А сам Роутер (на уровне App) уже решает, какой именно экран показать.
//
//---
//
//### ⛔️ Что КАТЕГОРИЧЕСКИ НЕЛЬЗЯ в слое Presentation
//1.  **НЕЛЬЗЯ вызывать Repositories напрямую из ViewModel.** Это прямое нарушение Clean Architecture. ViewModel общается с внешним миром ТОЛЬКО через Use Cases.
//2.  **НЕЛЬЗЯ писать бизнес-логику.** Если тебе нужно проверить, валидный ли email ввел пользователь, эта проверка (RegEx) должна лежать в `ValidateEmailUseCase` (Слой Domain), а не во ViewModel. ViewModel лишь вызывает этот UseCase и показывает красную рамку, если он вернул ошибку.
//3.  **НЕЛЬЗЯ использовать Singleton-роутеры.** Избегай глобальных сущностей вроде `Router.shared.push(...)`. Роутер должен передаваться через окружение (Environment), как мы это сделали через `Bindable`.
//
//### ✅ Что МОЖНО и НУЖНО в слое Presentation
//1.  **Паттерн Convenience Init.** То, что мы обсуждали: два инициализатора для `@MainActor` классов, чтобы избежать ворнингов компилятора и облегчить тестирование.
//2.  **Деление на модули (Фичи).** Если проект большой, папку Presentation разбивают на независимые фичи: `CartFeature`, `CatalogFeature`, `ProfileFeature`.
//
//---
//
//Вот мы и собрали весь пазл: **Domain** диктует правила, **Data** работает с "железом" и сетью, а **Presentation** красиво упаковывает это для пользователя.
//
