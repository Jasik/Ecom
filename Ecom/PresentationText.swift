//
//### 🎨 Идеальная шпаргалка: Слой PRESENTATION (UI и Навигация)
//
//Если **Domain** — это «мозг» (правила), а **Data** — это «руки» (добыча информации), то слой **Presentation (Презентация)** — это **«Лицо» и «Голос»** твоего приложения.
//
//Главная задача этого слоя — красиво показать данные пользователю и правильно отреагировать на его действия (нажатия, свайпы). Слой Presentation **не принимает бизнес-решений** и **не знает, откуда берутся данные** (из сети или локальной БД). Он работает исключительно с готовым результатом от Use Cases.
//
//В нашей архитектуре этот слой состоит из трех главных элементов: **Views**, **ViewModels** и **Router**.
//
//#### 1. Views (Экраны / SwiftUI)
//**Что это:** Визуальные компоненты (кнопки, списки, текст).
//**Для чего:** Отрисовывать состояние (State), которое предоставляет ViewModel, и передавать действия пользователя.
//
//**Правила написания:**
//* **«Тупой UI» (Dumb UI):** View должна быть максимально глупой. Она не умеет считать суммы, не умеет форматировать даты и не знает про сеть. Она просто рисует то, что ей дали.
//* **Изоляция SwiftUI:** Это единственное место во всем приложении, где мы пишем `import SwiftUI`.
//* **Обертка для Превью:** Мы больше не пишем костыли в самих View. Для безопасного рендера Канваса без крашей мы используем `PreviewWithDependencies`.
//
//```swift
//// ИДЕАЛЬНАЯ VIEW
//@MainActor
//struct ProductRowView: View {
//    let product: Product // Принимаем готовую, чистую модель
//    
//    var body: some View {
//        VStack {
//            Text(verbatim: product.title).font(.headline)
//            Text(verbatim: product.formattedPrice).foregroundColor(.blue) // Берем готовую строку
//        }
//    }
//}
//
//// ИДЕАЛЬНОЕ ПРЕВЬЮ
//#Preview("Корзина") {
//    PreviewWithDependencies { deps in
//        deps.cartRepo = MockEmptyCartRepository() // Подменяем на лету!
//    } content: {
//        NavigationStack { CartView() }
//            .environment(ShopRouter()) // Всегда внедряем Роутер!
//    }
//}
//```
//
//#### 2. ViewModels (Состояние и Логика экрана)
//**Что это:** Классы, которые служат "переводчиками" между View и Domain.
//**Для чего:** Хранят состояние UI (`var isLoading`, `var items`) и дергают UseCases.
//
//**Правила написания Swift 6:**
//* **`@MainActor @Observable`:** Вся работа с UI-состоянием должна происходить на главном потоке. Макрос `@Observable` работает быстрее старого `Combine` и перерисовывает только то, что реально изменилось.
//* **Прямое внедрение (Без `init`):** Зависимости внедряются изящно через наш DI.
//* **Защита от перерисовок (`@ObservationIgnored`):** Макрос `@Observable` пытается следить за всем. Чтобы SwiftUI не пытался "наблюдать" за инструментами бизнес-логики, мы обязательно ставим `@ObservationIgnored` перед `@Injected`.
//
//```swift
//// ИДЕАЛЬНАЯ VIEWMODEL (Без импорта SwiftUI!)
//import Foundation
//
//@MainActor @Observable
//public final class CartViewModel {
//    public var items: [Product] = [] // Состояние для View
//    
//    // 🪄 Магия DI: Внедряем UseCases одной строкой.
//    // @ObservationIgnored говорит SwiftUI: "Не следи за этим свойством!"
//    @ObservationIgnored @Injected(\.observeCartItemsUseCase) private var observeCartItems
//    @ObservationIgnored @Injected(\.removeFromCartUseCase) private var removeFromCart
//    
//    public init() {}
//    
//    // Реактивное прослушивание данных
//    public func startObserving() async {
//        for await newItems in await observeCartItems.execute() {
//            self.items = newItems // Обновляем UI
//        }
//    }
//}
//```
//
//#### 3. Router (Навигация)
//**Что это:** Класс, который управляет переходами между экранами, живущий в `.environment()`.
//**Для чего:** Чтобы убрать жесткую связность экранов друг с другом. `CatalogView` не должен знать про `CartView`. С Роутером экран просто говорит: `router.push(.productDetail)`, а сам Роутер решает, как это отрисовать. Никаких хардкодных `NavigationLink(destination:)`!
//
//---
//
//### ⛔️ Что КАТЕГОРИЧЕСКИ НЕЛЬЗЯ в слое Presentation
//1. **НЕЛЬЗЯ вызывать Repositories напрямую из ViewModel.** Это прямое нарушение Clean Architecture. ViewModel общается с внешним миром ТОЛЬКО через Use Cases.
//2. **НЕЛЬЗЯ забывать `@ObservationIgnored` перед `@Injected`.** Иначе компилятор выдаст ошибку конфликта оберток.
//3. **НЕЛЬЗЯ забывать `task.cancel()`.** Если экран слушает `AsyncStream` (поток данных), при закрытии экрана задача обязана быть отменена (например, автоматическая отмена внутри `.task { ... }`), иначе произойдет утечка памяти.
//4. **НЕЛЬЗЯ писать `return` внутри обычного `#Preview`.** Из-за макроса `@ViewBuilder` это ломает компилятор. Всегда используй `PreviewWithDependencies`.
//5. **НЕЛЬЗЯ писать бизнес-логику.** Если нужно проверить email на валидность (RegEx), эта проверка должна лежать в `ValidateEmailUseCase` (Domain), а не во ViewModel.
//
//### ✅ Что МОЖНО и НУЖНО
//1. **Использовать `async let` для параллелизма.** Если экрану при загрузке нужно и скачать данные, и запустить прослушивание стрима, запускай их параллельно, чтобы не блокировать потоки.
//2. **Деление на модули (Фичи).** Если проект большой, папку Presentation разбивают на независимые фичи: `CartFeature`, `CatalogFeature`, `ProfileFeature`.
//
