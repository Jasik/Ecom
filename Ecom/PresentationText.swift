//#### 2. ViewModels (Состояние и Логика экрана)
//**Что это:** Классы на макросе `@Observable` под защитой `@MainActor`.
//**Для чего:** Хранят состояние UI (`var isLoading`, `var items`) и дергают UseCase'ы.
//
//**Новое архитектурное свойство (Заморозка DI):** Раньше SwiftUI мог асинхронно перерисовать экран, и зависимость могла "слететь". Теперь, благодаря обновленному `@Injected(resolvedValue:)`, в момент вызова `init` у ViewModel все её UseCase'ы "фотографируют" (кэшируют) нужные репозитории. Это делает поток данных железобетонным.
//
//```swift
//@MainActor @Observable
