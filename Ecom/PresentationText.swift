//    private let observeCartItems: ObserveCartItemsUseCase
//    
//    public func startObserving() async {
//        // Мы используем AsyncStream для реактивного прослушивания
//        for await items in await observeCartItems.execute() {
//            self.items = items
//        }
//    }
//}
//```
//
//#### 3. Router (Навигация)
//**Что это:** Отдельный класс в `.environment()`. Экраны вызывают `router.push(.productDetail)`, а Роутер сам решает, как это отрисовать. Никаких хардкодных `NavigationLink(destination:)`!
