import Foundation

//Отличный вопрос про `.task`! Давай сначала разберем эту магию параллельного выполнения, а затем я выдам тебе финальную, отполированную шпаргалку по слою Presentation.
//
//### 🕵️‍♂️ Разбор магии: Зачем нужен `async let` в `CatalogView`?
//
//У тебя в `CatalogView.swift` написан вот такой код:
//```swift
//.task {
//    async let fetchProducts: () = vm.load()
//    async let listenCart: () = vm.observeCart()
//    _ = await (fetchProducts, listenCart)
//}
//```
//Это великолепный пример использования **Структурированной конкурентности (Structured Concurrency)** в Swift. Давай представим, что мы бы написали это обычным (последовательным) способом:
//
//**Обычный способ (с багом):**
//```swift
//.task {
//    await vm.observeCart() // ❗️ Бесконечный цикл!
//    await vm.load()        // Этот код НИКОГДА не выполнится!
//}
//```
//Так как `vm.observeCart()` слушает `AsyncStream` через `for await`, этот метод работает бесконечно (пока открыт экран). Если написать `await` перед ним, поток заблокируется, и товары (`vm.load()`) никогда не загрузятся.
//
//Если поменять их местами (`await vm.load()`, а затем `await vm.observeCart()`), то корзина начнет обновляться только *после* того, как скачаются все товары. Это медленно.
//
//**Решение с `async let`:**
//`async let` позволяет запустить две тяжелые асинхронные задачи **параллельно** (одновременно), не дожидаясь их завершения.
//Мы говорим Swift: *"Запусти скачивание товаров и одновременно с этим начни слушать корзину"*. А строчка `_ = await (fetchProducts, listenCart)` просто связывает эти параллельные задачи с жизненным циклом экрана. Если экран закроется, обе задачи будут мгновенно и безопасно отменены!
//
