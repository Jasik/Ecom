//#### ⛔️ Что КАТЕГОРИЧЕСКИ НЕЛЬЗЯ в слое Presentation (Обновлено)
//1. **НЕЛЬЗЯ писать `return` внутри обычного `#Preview`.** Из-за макроса `@ViewBuilder` это ломает компилятор. Всегда используй обертку `PreviewWithDependencies`.
//2. **НЕЛЬЗЯ забывать `task.cancel()`.** Если экран слушает `AsyncStream` (поток данных), при закрытии экрана задача обязана быть отменена, иначе произойдет утечка памяти (экран закроется, а корзина продолжит слушаться в фоне).
//3. **НЕЛЬЗЯ вызывать Repositories напрямую.** ViewModel общается с внешним миром ТОЛЬКО через Use Cases.
