//### ⚡️ Deep Dive: Concurrency (Многопоточность в слое Data)
//
//Чтобы слой Data работал быстро и не крашил приложение, мы используем два главных столпа современной конкурентности (Concurrency) в Swift.
//
//#### 🛡 1. `actor` (Актор) — Личный охранник твоих данных
//Актор — это ссылочный тип (как `class`), но со **встроенной, автоматической очередью блокировок**.
//
//* **Как это работает (Аналогия):** Представь, что твоя переменная `cartItems` — это единственный кулер с водой в офисе. Потоки (Threads) — это сотрудники. Если два сотрудника попытаются налить воду одновременно, вода прольется (Data Race / Crash). Актор — это умный охранник. Он выстраивает сотрудников в очередь. Если кто-то наливает воду, остальные ждут (для этого мы пишем `await`).
//* **Когда использовать:** Когда есть изменяемое состояние (`var`), к которому обращаются из разных потоков (UI-поток и фоновый поток сети). Например: `LiveCartRepository` (хранит корзину) или `ImageCacheManager` (хранит картинки).
//* **Когда НЕ использовать:** Если репозиторий просто скачивает данные и отдает их (как `LiveProductRepository`), актор создаст ненужную очередь и замедлит работу. Используй `struct`.
//
//#### 🌊 2. `AsyncStream` — Асинхронная река данных
//`AsyncStream` — это нативная замена Combine (`@Published`) для мира `async/await`.
//
//* **Как это работает:** Это труба, в которую кто-то (Актор) закидывает данные, а кто-то другой (ViewModel) стоит на другом конце трубы и ловит эти данные через цикл `for await`.
//* **Когда использовать:** Когда данные приходят во времени (Continuous data). Например: слушать изменения в корзине, слушать координаты GPS, слушать WebSockets (чат).
//* **Когда НЕ использовать:** Для разовых запросов (One-shot). Чтобы просто скачать каталог товаров один раз, достаточно обычной функции `func getProducts() async throws -> [Product]`.
//
//#### 🤝 Идеальная связка: `actor` + `AsyncStream`
//Вся магия нашей архитектуры (как видно в `LiveCartRepository`) заключается в том, как эти два инструмента работают вместе:
//
//1. **Актор защищает данные:** Он гарантирует, что никто не сломает массив `cartItems` при одновременном добавлении.
//2. **Актор отправляет уведомления:** Внутри актора мы прячем `continuation` (пульт управления стримом). Как только данные меняются, актор безопасно дергает за рубильник: `continuation.yield(newData)`.
//3. **ViewModel слушает поток:** В слое Presentation мы запускаем `Task`, который бесконечно "вычерпывает" данные из реки: `for await items in stream { ... }`. При закрытии экрана задача автоматически отменяется, стрим перекрывается, и память очищается!
