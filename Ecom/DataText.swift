//### 🗄 Идеальная шпаргалка: Слой DATA (Данные) и Concurrency
//
//Если **Domain (Домен)** — это "мозг" и "директор" приложения, который раздает приказы, то слой **Data (Данные)** — это "руки" и "чернорабочие".
//
//Домен говорит: *"Мне нужен список товаров"*. Домену абсолютно плевать, откуда они возьмутся.
//Слой Data отвечает: *"Понял! Сейчас я создам `URLRequest`, схожу на сервер `dummyjson.com`, скачаю байты, распаршу JSON, отфильтрую лишний мусор, обработаю ошибку отсутствия интернета и верну тебе готовый, чистый массив"*.
//
//Слой Data знает всё про интернет, базы данных, кэширование и файловую систему.
//
//#### 1. DTO (Data Transfer Objects / Сетевые Модели) — ❗️ *Главный щит приложения*
//**Что это:** Структуры, созданные исключительно для того, чтобы совпасть с JSON, который присылает сервер (`ProductDTO`, `ProductResponse`).
//**Для чего:** Сервер часто присылает технический мусор или данные в неудобном формате. DTO принимает этот удар на себя, а затем с помощью метода-маппера перекладывает нужные данные в чистую доменную модель.
//
//```swift
//// ИДЕАЛЬНЫЙ DTO (Слой Data)
//struct ProductDTO: Decodable, Sendable {
//    let id: Int
//    let title: String
//    let price: Double
//    let unused_meta: String // Мусор с бэкенда, который не пойдет дальше
//    
//    // 🪄 Маппер: технический объект -> бизнес-объект
//    func toDomain() -> Product {
//        Product(id: id, title: title, description: "", price: price, images: [], thumbnail: "")
//    }
//}
//```
//
//#### 2. Repositories (Репозитории — Реализации)
//**Что это:** Исполнители контрактов (протоколов) из слоя Domain. Они являются мостом между логикой и "железом".
//**Для чего:** Ходят в сеть или базу данных, получают DTO, мапят их в Domain-модели и отдают UseCase'ам.
//
//В Swift 6 мы разделяем репозитории на два типа в зависимости от того, хранят ли они состояние:
//
//**Пример 1: Репозиторий без состояния (Stateless) = `struct`**
//Наш `LiveProductRepository` не хранит в себе ничего. Он просто берет инструмент из DI, пересылает запрос в сеть, мапит ответ и возвращает его. Использование структуры экономит ресурсы процессора.
//
//```swift
//// РЕПОЗИТОРИЙ БЕЗ СОСТОЯНИЯ (Слой Data)
//struct LiveProductRepository: ProductRepository {
//    // Безопасно достаем абстрактный сетевой клиент
//    @Injected(\.apiClient) private var api
//    
//    func getProducts() async throws -> [Product] {
//        // 1. Скачиваем DTO (технический ответ сервера)
//        let response: ProductResponse = try await api.fetch(url: "https://dummyjson.com/products")
//        
//        // 2. ❗️ Обязательно мапим DTO в чистую бизнес-модель
//        return response.products.map { $0.toDomain() }
//    }
//}
//```
//
//**Пример 2: Репозиторий с состоянием (Stateful) = `actor`**
//Наш `LiveCartRepository` хранит внутри массив `var cartItems`. Чтобы два потока одновременно не попытались изменить этот массив (что вызовет краш приложения), мы обязаны использовать актор для защиты от гонок данных.
//
//```swift
//// РЕПОЗИТОРИЙ С СОСТОЯНИЕМ (Слой Data)
//actor LiveCartRepository: CartRepository {
//    // Защищенное состояние (никто извне не может изменить его напрямую)
//    private var cartItems: [Product] = []
//    
//    // Пульт управления для реки данных (AsyncStream)
//    private var itemsContinuation: AsyncStream<[Product]>.Continuation?
//    
//    func addToCart(product: Product) async {
//        cartItems.append(product)
//        
//        // Безопасно уведомляем всех слушателей (экраны) об изменении корзины
//        itemsContinuation?.yield(cartItems)
//    }
//    
//    // Реактивный метод для UI
//    func observeCartItems() async -> AsyncStream<[Product]> {
//        // Создаем "трубу" для передачи данных
//        let (stream, continuation) = AsyncStream<[Product]>.makeStream()
//        self.itemsContinuation = continuation
//        
//        // Сразу "выплевываем" в трубу текущее состояние для первого рендера экрана
//        continuation.yield(cartItems)
//        
//        return stream
//    }
//}
//```
