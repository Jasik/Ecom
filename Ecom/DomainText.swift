import Foundation

//Отличная идея! Давай объединим всё лучшее из двух вариантов, уберем устаревшие детали и создадим идеальную, актуальную шпаргалку по слою **Domain**, которая будет на 100% соответствовать твоему текущему коду магазина.
//
//Это готовый материал, который можно смело сохранять себе в заметки или `README.md` проекта!
//
//---
//
//### 🧠 Идеальная шпаргалка: Слой Domain (Бизнес-логика)
//
//Слой **Domain (Домен / Бизнес-логика)** — это самое сердце приложения, его "VIP-зона" и интеллектуальный центр. Главная идея этого слоя заключается в том, что он **вообще ничего не знает о внешнем мире**. Ему абсолютно неважно, как выглядит дизайн (UI), откуда берутся данные (REST API, Firebase или локальная БД Realm) и на какой платформе работает приложение.
//
//Если завтра Apple удалит SwiftUI и выпустит новый фреймворк для UI, слой Domain **не должен измениться ни на одну строчку кода**.
//
//Слой базируется на трех главных китах: **Models**, **Interfaces** и **UseCases**.
//
//---
//
//#### 1. Models (Модели / Сущности)
//**Что это:** Чистые структуры данных, описывающие объекты реального мира в рамках твоего бизнеса (в нашем случае — магазина).
//**Для чего:** Они задают единый "словарь" приложения. Все остальные слои (и UI, и Data) обязаны конвертировать свои данные и общаться с Доменом только с помощью этих Моделей.
//
//**Правила написания:**
//* **Идеальная чистота:** Никакого протокола `Decodable` или `Encodable`. Модель не должна знать о том, что такое JSON. Парсингом занимаются DTO в слое Data.
//* **Никакого UI:** Внутри модели нет логики для отрисовки (например, форматирование цены со знаком `$` мы выносим в `extension` или ViewModel).
//* **Потокобезопасность (Swift 6):** Все модели обязаны быть `Sendable`, чтобы безопасно летать между фоновыми потоками и потоком интерфейса.
//
//```swift
//// ИДЕАЛЬНАЯ МОДЕЛЬ (Только чистые бизнес-данные)
//public struct Product: Identifiable, Hashable, Sendable {
//    public let id: Int
//    public let title: String
//    public let description: String
//    public let price: Double
//    public let images: [String]
//    public let thumbnail: String
//}
//```
//
//---
//
//#### 2. Interfaces (Интерфейсы / Протоколы / Контракты)
//**Что это:** Набор строгих требований. Домен говорит: *"Я не знаю, как вы это реализуете, но мне нужен инструмент, который умеет отдавать список товаров и сохранять их в корзину"*.
//**Для чего:** Это защита через инверсию зависимостей (Dependency Inversion). Благодаря протоколам наш бизнес-слой не зависит от конкретных библиотек или классов вроде `LiveCartRepository`.
//
//**Правила написания:**
//* **Никакой реализации:** Мы пишем только сигнатуры методов (`func ...`).
//* **Словарь Домена:** Протоколы должны принимать и возвращать ТОЛЬКО доменные Модели (никаких DTO, сырых `Data` или сетевых ошибок).
//
//```swift
//// ИДЕАЛЬНЫЙ ИНТЕРФЕЙС
//public protocol CartRepository: Sendable {
//    func addToCart(product: Product) async
//    func observeCartItems() async -> AsyncStream<[Product]>
//}
//```
//
//---
//
//#### 3. Use Cases (Сценарии использования / Интеракторы)
//**Что это:** Конкретные действия, которые пользователь может совершить в приложении. Один `UseCase` = одно бизнес-действие.
//**Для чего:** Это дирижеры потока данных. Они берут инструменты из Интерфейсов, применяют к ним бизнес-правила (например, проверки на наличие товара) и отдают результат экрану.
//
//**Правила написания:**
//* **Один метод:** Обычно UseCase имеет только один публичный метод (часто его называют `execute()` или `callAsFunction()`).
//* **Замороженный DI:** UseCase получает нужные репозитории через наш DI-контейнер (`@Injected`). Зависимость безопасно "замораживается" в момент инициализации, защищая логику от сбоев в асинхронной среде.
//
//```swift
//// ИДЕАЛЬНЫЙ USE CASE
//public struct AddToCartUseCase: Sendable {
//    // Безопасно получаем инструмент из слоя Data (ничего не зная о его реализации)
//    @Injected(\.cartRepo) private var repo
//    
//    public init() {}
//    
//    public func execute(_ product: Product) async {
//        // Здесь может быть бизнес-логика: логирование, проверка лимитов корзины и т.д.
//        await repo.addToCart(product: product)
//    }
//}
//```
//
//---
//
//#### ⛔️ Что КАТЕГОРИЧЕСКИ НЕЛЬЗЯ в слое Domain
//Это самые частые ошибки, за которые на код-ревью сразу возвращают задачу на доработку:
//1. **НЕЛЬЗЯ `import SwiftUI` или `import UIKit`.** Домен не должен знать про `Color`, `Font`, View-компоненты или анимации. Допустим только `import Foundation`.
//2. **НЕЛЬЗЯ импортировать сторонние SDK.** Никакого `import Alamofire`, `import Firebase` или `import Realm`. Если внешняя библиотека обновится и сломается, твое ядро не должно пострадать.
//3. **НЕЛЬЗЯ хранить состояние (State).** UseCases должны быть независимыми структурами (stateless) без изменяемых переменных (`var`), которые могут сохранять мусор между вызовами.
//4. **НЕЛЬЗЯ писать сетевые запросы и парсинг.** Внутри Домена не должно быть работы с `URL`, HTTP-статусами или `JSONDecoder`. Это грязная работа, которую выполняет слой Data.
//
//#### ✅ Что МОЖНО и НУЖНО в слое Domain
//1. **Чистый Swift.** Простые типы данных, базовая математика, бизнес-проверки и выброс доменных ошибок (`if price < 0 { throw CartError.invalidPrice }`).
//2. **Абсолютное покрытие Unit-тестами.** Так как здесь нет UI и реальной сети, логика UseCases легко покрывается тестами с использованием Mock-репозиториев. Тесты выполняются за миллисекунды со 100% надежностью.
